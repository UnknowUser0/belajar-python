# Operator Penjelasan Contoh
#
# =
# Menugaskan nilai yang ada di kanan ke operand yang ada
# di sebelah kiri
# c = a + b
# menugaskan a + b
# ke c
#
# +=
# Menambahkan operand yang di kanan dengan operand
# yang ada di kiri dan hasilnya di tugaskan ke operand yang
# di kiri
# c += a sama dengan
# c = c + a
#
# -=
# Mengurangi operand yang di kanan dengan operand yang
# ada di kiri dan hasilnya di tugaskan ke operand yang di
# kiri
# c -= a sama dengan
# c = c + a
#
# *=
# Mengalikan operand yang di kanan dengan operand yang
# ada di kiri dan hasilnya di tugaskan ke operand yang di
# kiri
# c *= a sama dengan
# c = c * a
#
# /=
# Membagi operand yang di kanan dengan operand yang
# ada di kiri dan hasilnya di tugaskan ke operand yang di
# kiri
# c /= a sama dengan
# c = c * a
#
# **=
# Memangkatkan operand yang di kanan dengan operand
# yang ada di kiri dan hasilnya ditugaskan ke operand yang
# di kiri
# c **= a sama
# dengan c = c ** a
#
# //=
# Melakukan pembagian bulat operand di kanan terhadap
# operand di kiri dan hasilnya disimpan di operand yang di
# kiri
# c //= a sama dengan
# c = c // a
#
# %=
# Melakukan operasi sisa bagi operand di kanan dengan
# operand di kiri dan hasilnya di simpan di operand yang di
# kiri
# c %= a sama
# dengan c = c % a